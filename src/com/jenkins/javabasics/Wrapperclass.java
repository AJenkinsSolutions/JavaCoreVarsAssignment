package com.jenkins.javabasics;

public class Wrapperclass {

	public static void main(String[] args) {
		/**
		 * Examples of Wrapper class and respective 
		 */
		byte b = 100;
		Byte bbByte = 100;
		
		short s = 100;
		Short ss = 100;
		
		int i = 100; //Primitive Data type
		Integer ii = 100; //Literal method
		Integer iii = new Integer(1000); // new operator 
		
		long l = 100l;
		Long ll = 100l;
		
		float f = 100.0f;
		Float ff = 100.0f;
		
		double d = 535.55;
		Double dd = 545.33;
		
		char ch = 'a';
		Character cha = 'a';
		
		boolean bo = true;
		Boolean boolean1 = true; 
		
		
	}

}
